class TestSaveLoadLocs:
    def test_roundtrip(self, tmp_path, locs, info):
        path = tmp_path / "locs.hdf5"
        io.save_locs(str(path), locs, info)
        # Side-effect: a .yaml is written alongside the .hdf5
        assert (tmp_path / "locs.yaml").exists()

        loaded, loaded_info = io.load_locs(str(path))
        # Both go through ensure_sanity → row counts may match exactly
        assert len(loaded) == len(locs)
        assert set(loaded.columns) == set(locs.columns)
        # Spot-check numerical content
        np.testing.assert_allclose(
            loaded["x"].to_numpy(), locs["x"].to_numpy()
        )

    def test_csv_extension_raises(self, tmp_path):
        # ThunderSTORM .csv files must go through import_ts, not load_locs
        path = tmp_path / "locs.csv"
        path.write_text("dummy")
        with pytest.raises(ValueError, match="ThunderSTORM"):
            io.load_locs(str(path))


class TestSaveDatasets:
    def test_writes_named_datasets(self, tmp_path):
        path = tmp_path / "datasets.hdf5"
        a = pd.DataFrame({"x": [1.0, 2.0], "y": [3.0, 4.0]})
        b = pd.DataFrame({"x": [10.0], "y": [20.0]})
        info = [{"Width": 32, "Height": 32, "Frames": 1}]
        io.save_datasets(str(path), info, locs=a, picks=b)

        with h5py.File(path, "r") as f:
            assert "locs" in f
            assert "picks" in f
            assert f["locs"].shape == (2,)
            assert f["picks"].shape == (1,)


