class TestDriftRoundtrip:
    def test_2d_roundtrip(self, tmp_path):
        drift = pd.DataFrame(
            {
                "x": np.linspace(0.0, 1.0, 50),
                "y": np.linspace(0.0, -0.5, 50),
            }
        )
        path = tmp_path / "drift.txt"
        io.save_drift(str(path), drift)
        loaded = io.load_drift(str(path))
        assert list(loaded.columns) == ["x", "y"]
        np.testing.assert_allclose(
            loaded["x"].to_numpy(), drift["x"].to_numpy()
        )
        np.testing.assert_allclose(
            loaded["y"].to_numpy(), drift["y"].to_numpy()
        )

    def test_3d_roundtrip(self, tmp_path):
        drift = pd.DataFrame(
            {
                "x": np.linspace(0.0, 1.0, 30),
                "y": np.linspace(0.0, -0.5, 30),
                "z": np.linspace(-2.0, 2.0, 30),
            }
        )
        path = tmp_path / "drift.txt"
        io.save_drift(str(path), drift)
        loaded = io.load_drift(str(path))
        assert "z" in loaded.columns
        np.testing.assert_allclose(
            loaded["z"].to_numpy(), drift["z"].to_numpy()
        )

    def test_non_txt_extension_raises(self, tmp_path):
        with pytest.raises(ValueError):
            io.load_drift(str(tmp_path / "drift.csv"))


