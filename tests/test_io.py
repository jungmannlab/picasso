class TestThunderstormRoundtrip:
    def test_export_import_2d(self, tmp_path):
        locs = pd.DataFrame(
            {
                "frame": np.array([0, 1, 2], dtype=np.uint32),
                "x": np.array([5.0, 10.0, 15.0], dtype=np.float32),
                "y": np.array([5.0, 10.0, 15.0], dtype=np.float32),
                "sx": np.array([1.0, 1.0, 1.0], dtype=np.float32),
                "sy": np.array([1.0, 1.0, 1.0], dtype=np.float32),
                "photons": np.array(
                    [1000.0, 2000.0, 3000.0], dtype=np.float32
                ),
                "bg": np.array([10.0, 10.0, 10.0], dtype=np.float32),
                "lpx": np.array([0.05, 0.05, 0.05], dtype=np.float32),
                "lpy": np.array([0.05, 0.05, 0.05], dtype=np.float32),
            }
        )
        info = [
            {"Pixelsize": PIXELSIZE, "Width": 64, "Height": 64, "Frames": 3}
        ]
        ts_path = tmp_path / "ts.csv"
        io.export_thunderstorm(str(ts_path), locs, info)

        # import_ts also writes a sibling _locs.hdf5 — the round-trip we
        # care about here is the in-memory DataFrame.
        out_locs, out_info = io.import_ts(str(ts_path), pixelsize=PIXELSIZE)
        # x/y conversion: nm → pixels via /pixelsize, should match input
        np.testing.assert_allclose(
            out_locs["x"].to_numpy(), locs["x"].to_numpy(), atol=1e-3
        )
        np.testing.assert_allclose(
            out_locs["y"].to_numpy(), locs["y"].to_numpy(), atol=1e-3
        )
        # Frame count is preserved (frames re-zeroed by import_ts)
        assert out_info[0]["Frames"] == 3
