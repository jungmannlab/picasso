class TestRawRoundtrip:
    def _synthetic_movie(self):
        rng = np.random.default_rng(0)
        movie = rng.integers(0, 65535, size=(4, 6, 8), dtype=np.uint16)
        info = {
            "Byte Order": "<",
            "Data Type": "uint16",
            "Frames": 4,
            "Height": 6,
            "Width": 8,
            "Generated by": "test_io",
        }
        return movie, info

    def test_save_raw_roundtrip(self, tmp_path):
        movie, info = self._synthetic_movie()
        path = tmp_path / "movie.raw"
        io.save_raw(str(path), movie, [info])
        # save_raw writes both .raw and .yaml
        assert path.exists()
        assert (tmp_path / "movie.yaml").exists()

        loaded, loaded_info = io.load_raw(str(path))
        np.testing.assert_array_equal(np.asarray(loaded), movie)
        assert loaded_info[0]["Frames"] == 4

    def test_load_movie_dispatches_raw(self, tmp_path):
        movie, info = self._synthetic_movie()
        path = tmp_path / "movie.raw"
        io.save_raw(str(path), movie, [info])
        loaded, loaded_info = io.load_movie(str(path))
        np.testing.assert_array_equal(np.asarray(loaded), movie)


