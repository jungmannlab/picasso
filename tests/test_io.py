class TestSaveLoadInfo:
    def test_roundtrip_preserves_list_order(self, tmp_path):
        info = [
            {"Width": 64, "Height": 32, "Frames": 100},
            {"Box Size": 7, "Min. Net Gradient": 5000},
        ]
        path = tmp_path / "info.yaml"
        io.save_info(str(path), info)
        loaded = io.load_info(str(path))
        assert loaded == info

    def test_load_info_missing_raises(self, tmp_path):
        # load_info derives the .yaml path from a movie path
        with pytest.raises(io.NoMetadataFileError):
            io.load_info(str(tmp_path / "no_such_movie.raw"))


class TestToDictWalk:
    def test_converts_autodict_recursively(self):
        ad = lib.AutoDict()
        ad["a"]["b"]["c"] = 1
        ad["x"] = 2
        out = io._to_dict_walk(ad)
        assert isinstance(out, dict)
        assert isinstance(out["a"], dict)
        assert isinstance(out["a"]["b"], dict)
        assert out["a"]["b"]["c"] == 1
        assert out["x"] == 2


